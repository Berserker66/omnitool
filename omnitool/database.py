def get_custom_db(txt):
    from omnitool import colorlib

    colors = colorlib.data.copy()
    wallcolors = colorlib.walldata.copy()
    ntiles = {}
    x = 0
    for t in tiles:
        ntiles[x] = t
        x += 1
    nwalls = {}
    x = 0
    for t in walls:
        nwalls[x] = t
        x += 1
    nmultitiles = multitiles[:]
    for t in txt["tiles"]:
        ntiles[t[0]] = t[1]
        if t[2]: nmultitiles.append(t[0])
        colors[t[0]] = t[4]
    for w in txt["walls"]:
        nwalls[w[0]] = w[1]
        wallcolors[w[0]] = w[2]
    return ntiles, nwalls, nmultitiles, colors, wallcolors


