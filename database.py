def get_custom_db(txt):
    import colorlib

    colors = colorlib.data.copy()
    wallcolors = colorlib.walldata.copy()
    ntiles = {}
    x = 0
    for t in tiles:
        ntiles[x] = t
        x += 1
    nwalls = {}
    x = 0
    for t in walls:
        nwalls[x] = t
        x += 1
    nmultitiles = multitiles[:]
    for t in txt["tiles"]:
        ntiles[t[0]] = t[1]
        if t[2]: nmultitiles.append(t[0])
        colors[t[0]] = t[4]
    for w in txt["walls"]:
        nwalls[w[0]] = w[1]
        wallcolors[w[0]] = w[2]
    return ntiles, nwalls, nmultitiles, colors, wallcolors


def parse():
    for t in otiles:
        try:
            tiles.append(unicode(t))
        except NameError:
            tiles.append(t)
    for t in owalls:
        try:
            walls.append(unicode(t))
        except NameError:
            walls.append(t)

    i = 0
    for name in tiles:
        ntiles[name] = i
        i += 1
    i = 0

    for name in walls:
        nwalls[name] = i
        i += 1

    for m in multitiles:
        nmultitiles.append(tiles[m])


